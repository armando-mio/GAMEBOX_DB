--
-- QUERY 1: clienti che hanno acquistato di più online
--
SELECT ACQUISTA.CF_CLIENTE, sum(case when ORDINE.ONLINE_='1' THEN 1 ELSE 0 END) AS TOT_ORDINI
FROM ACQUISTA INNER JOIN ORDINE ON ACQUISTA.ORDINE=ORDINE.COD
GROUP BY CF_CLIENTE
ORDER BY TOT_ORDINI DESC
LIMIT 10

--
-- QUERY 2: bilancio
--
SELECT COUNT(ACQUISTA.ORDINE) AS TOT_ORDINI, SUM(PRODOTTO.PREZZO) AS LORDO
FROM PRODOTTO INNER JOIN ACQUISTA ON PRODOTTO.COD=ACQUISTA.PRODOTTO
			  INNER JOIN ORDINE ON ORDINE.COD=ACQUISTA.ORDINE
WHERE PRODOTTO.COD=ACQUISTA.PRODOTTO
	AND ORDINE.DATA_ACQUISTO BETWEEN '2022-01-01' AND '2022-06-30'

--
-- QUERY 3: genere acquistati da clienti
--
SELECT VIDEOGIOCO.GENERE, COUNT(ACQUISTA.ORDINE) AS GIOCHI_VENDUTI, SUM(PRODOTTO.PREZZO) AS INCASSO
FROM ACQUISTA INNER JOIN PRODOTTO ON PRODOTTO.COD=ACQUISTA.PRODOTTO
              INNER JOIN VIDEOGIOCO ON PRODOTTO.COD=VIDEOGIOCO.COD_PRODOTTO
GROUP BY VIDEOGIOCO.GENERE

--
-- QUERY 4: numero di abbonati lv3 che accedono alle varie sale lan
--
SELECT ACCEDERE.SALA_LAN, COUNT(ACCEDERE.ABBONATO) AS NUMERO_ABBONATI
FROM ACCEDERE INNER JOIN CLIENTE ON ACCEDERE.ABBONATO=CLIENTE.CF
WHERE CLIENTE.TESSERA='LV3'
GROUP BY ACCEDERE.SALA_LAN
ORDER BY NUMERO_ABBONATI DESC

--
-- QUERY 5: quanto pagano i clienti che accedono alla lan_party divisi per omaggi offerti
--
SELECT LAN_PARTY.OMAGGIO, COUNT(ACCEDERE.ABBONATO) AS TOT_PRESENTI, SUM(TESSERA.PREZZO) AS INCASSI_DA_TESSERA
FROM LAN_PARTY INNER JOIN ACCEDERE ON LAN_PARTY.SALA=ACCEDERE.SALA_LAN
			   INNER JOIN CLIENTE ON ACCEDERE.ABBONATO=CLIENTE.CF
               INNER JOIN TESSERA ON CLIENTE.TESSERA=TESSERA.LIVELLO
GROUP BY LAN_PARTY.OMAGGIO

--
-- QUERY 6: lista negozi con ordini fisici
--
SELECT DISTINCT NEGOZIO.COD, COUNT(ORDINE.COD) AS ORDINI_FISICI
FROM NEGOZIO INNER JOIN ORDINE ON NEGOZIO.COD=ORDINE.COD_NEGOZIO
WHERE ORDINE.ONLINE_='0'
 	AND NEGOZIO.COD=ORDINE.COD_NEGOZIO
GROUP BY NEGOZIO.COD

