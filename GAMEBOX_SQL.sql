--
-- CREAZIONE TABELLE
--
CREATE TABLE INDIRIZZO (
	COD VARCHAR(7),
	REGIONE VARCHAR(20) NOT null,
	PROVINCIA VARCHAR(2) NOT null,
	VIA VARCHAR(32) NOT null,
	CIVICO SMALLINT NOT null,
  	PRIMARY KEY(COD)
);

CREATE TABLE NEGOZIO (
	COD VARCHAR(6),
	ORARIO_APERTURA TIME NOT null,
	ORARIO_CHIUSURA TIME NOT null,
	DATA_APERTURA DATE NOT null,
	DATA_CHIUSURA DATE,
	INDIRIZZO VARCHAR(7),
  	PRIMARY KEY (COD),
	foreign key (INDIRIZZO) references INDIRIZZO(COD)	on update cascade on delete cascade
);
  
CREATE TABLE PRODOTTO(
	COD VARCHAR(10),
	PREZZO DECIMAL(6,2) NOT null,
	DISPONIBILITA SMALLINT NOT null,
	COD_NEGOZIO	VARCHAR(10),
  	PRIMARY KEY(COD),
	foreign key (COD_NEGOZIO) references NEGOZIO(COD)	on update cascade on delete cascade
);
	
CREATE TABLE PRODUTTORE (
	NOME VARCHAR(50),
	CAPITALE BIGINT NOT null,
	NAZIONALITA VARCHAR(20) NOT null,
	PRIMARY KEY(NOME)
);

CREATE TABLE VIDEOGIOCO (
	COD VARCHAR(10),
	NOME VARCHAR(50), 
	GENERE VARCHAR(20) NOT null,  
	DATA_USCITA DATE NOT null, 
	PEGI INTEGER NOT null, 
	VALUTAZIONE DECIMAL(5,2) NOT null, 
	ONLINE_ INTEGER NOT null, 
	PRODUTTORE VARCHAR(50),
	COD_PRODOTTO VARCHAR(10),
	PRIMARY KEY(COD),
	foreign key (PRODUTTORE) references PRODUTTORE(NOME) on update cascade on delete cascade,
	foreign key (COD_PRODOTTO) references PRODOTTO(COD) on update cascade on delete cascade
);

CREATE TABLE MERCHANDISE (
	COD VARCHAR(10),
	CATEGORIA VARCHAR(20) NOT null,
	NOME VARCHAR(100),
	COD_PRODOTTO VARCHAR(10),
	PRIMARY KEY(COD),
	foreign key (COD_PRODOTTO) references PRODOTTO(COD) on update cascade on delete cascade
);

CREATE TABLE CONSOLE (
	COD VARCHAR(10),
	MODELLO VARCHAR(50) NOT null,
    DATA_USCITA DATE NOT null,
	COLORE VARCHAR(15) NOT null,
	MEMORIA VARCHAR(10) NOT null,
    PRODUTTORE VARCHAR(50),
	COD_PRODOTTO VARCHAR(10),
	PRIMARY KEY(COD),
    foreign key (PRODUTTORE) references PRODUTTORE(NOME) on update cascade on delete cascade,
	foreign key (COD_PRODOTTO) references PRODOTTO(COD) on update cascade on delete cascade
);

CREATE TABLE ORDINE (
	COD VARCHAR(10),
	DATA_ACQUISTO DATE NOT null,
	METODO_PAGAMENTO VARCHAR(20) NOT null,
	SCONTO SMALLINT,
    ONLINE_ SMALLINT NOT null,
	COD_NEGOZIO VARCHAR(6),
	PRIMARY KEY(COD),
	foreign key (COD_NEGOZIO) references NEGOZIO(COD) on update cascade on delete cascade
);

CREATE TABLE SPEDIZIONE (
	NUMERO VARCHAR(6),
	CORRIERE VARCHAR(5) NOT null,
	COSTO DECIMAL(5,2) NOT null,
	ARRIVO_PREVISTO DATE NOT null,
	MODALITA VARCHAR(20) NOT null,
  	COD_ORDINE VARCHAR(5),
	INDIRIZZO VARCHAR(7), 
	PRIMARY KEY(NUMERO),
	foreign key (COD_ORDINE) references ORDINE(COD)	on update cascade on delete cascade,
	foreign key (INDIRIZZO) references INDIRIZZO(COD) on update cascade on delete cascade
);

CREATE TABLE TESSERA (
	LIVELLO VARCHAR(5),
	COLORE VARCHAR(15) NOT null,
	PREZZO DECIMAL(5,2) NOT null,
	PRIMARY KEY(LIVELLO)
);

CREATE TABLE CLIENTE (
	CF VARCHAR(10),
	NOME VARCHAR(20) NOT null,
	COGNOME VARCHAR(20) NOT null,
	EMAIL VARCHAR(50) NOT null,
	DATA_NASCITA DATE NOT null,
  	ABBONATO SMALLINT NOT null,
	PUNTI INTEGER,
	DATA_INIZIO DATE,
	DATA_FINE DATE,
	TESSERA VARCHAR(5),
	INDIRIZZO VARCHAR(7),
	PRIMARY KEY(CF),
	foreign key (TESSERA) references TESSERA(LIVELLO) on update cascade on delete cascade,
	foreign key (INDIRIZZO) references INDIRIZZO(COD) on update cascade on delete cascade
);

CREATE TABLE LAN_PARTY (
	SALA VARCHAR(10),
	OMAGGIO VARCHAR(20) NOT null,
	CONNESSIONE VARCHAR(7) NOT null,
	COD_NEGOZIO VARCHAR(7),
	PRIMARY KEY(SALA),
	foreign key (COD_NEGOZIO) references NEGOZIO(COD)	on update cascade on delete cascade
);

CREATE TABLE POSTAZIONE (
	NUMERO SMALLINT,
	SALA_LAN VARCHAR(10),
    CONSOLE VARCHAR(10),
	PRIMARY KEY(NUMERO, SALA_LAN),
	foreign key (SALA_LAN) references LAN_PARTY(SALA) on update cascade on delete cascade,
    foreign key (CONSOLE) references CONSOLE(COD) on update cascade on delete cascade
);
	
CREATE TABLE ACQUISTA (
	CF_CLIENTE VARCHAR(10),
	PRODOTTO VARCHAR(10),
	ORDINE VARCHAR(10),
	PRIMARY KEY (CF_CLIENTE, PRODOTTO, ORDINE),
	foreign key (CF_CLIENTE) references CLIENTE(CF) on update cascade on delete cascade,
	foreign key (PRODOTTO) references PRODOTTO(COD) on update cascade on delete cascade,
	foreign key (ORDINE) references ORDINE(COD) on update cascade on delete cascade
);

CREATE TABLE OFFERTA (
	COD VARCHAR(10),
	SCONTO SMALLINT NOT null,
	TESSERA VARCHAR(5),
	PRIMARY KEY(COD),
	foreign key (TESSERA) references TESSERA(LIVELLO) on update cascade on delete cascade
);

CREATE TABLE ACCEDERE (
    SALA_LAN VARCHAR(10),
    ABBONATO VARCHAR(10),
    PRIMARY KEY(SALA_LAN, ABBONATO),
    foreign key (SALA_LAN) references LAN_PARTY(SALA) on update cascade on delete cascade,
   	foreign key (ABBONATO) references CLIENTE(CF) on update cascade on delete cascade
);

CREATE TABLE PRESSO (
    COD_NEGOZIO VARCHAR(10),
    COD_PRODOTTO VARCHAR(10),
    PRIMARY KEY(COD_NEGOZIO, COD_PRODOTTO),
    foreign key (COD_NEGOZIO) references NEGOZIO(COD) on update cascade on delete cascade,
    foreign key (COD_PRODOTTO) references PRODOTTO(COD) on update cascade on delete cascade
);
  
